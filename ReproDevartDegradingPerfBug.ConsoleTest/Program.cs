      static async Task RunTest()
      {
         await MakeHttpRequest(WarmupUrl);
         for (var i = 0; i < 50; i++)
         {
            Thread.Sleep(5000);
            await MeasurePerformance();
            await RunLoadTest();
         }
      }

      static async Task MeasurePerformance()
      { 
         var sw = new Stopwatch();
         sw.Start();
         var response = await MakeHttpRequest(MeasurementUrl);
         sw.Stop();
         Console.WriteLine($"Measured time: {sw.ElapsedMilliseconds} ms. Status code {response.StatusCode}");
      }

      static async Task<bool> RunLoadTest()
      {
         var errors = false;
         foreach (var url in Urls)
         {
            try
            {
               var response = await MakeHttpRequest(url);
               if (response.StatusCode != HttpStatusCode.OK)
                  errors = true;
            }
            catch
            {
               errors = true;
            }
         }

         return errors;
      }

      static async Task<HttpResponseMessage> MakeHttpRequest(string url)
      {
         var client = new HttpClient();
         return await client.GetAsync(url);
      }

      private static readonly TaskFactory Factory = new
         TaskFactory(CancellationToken.None,
            TaskCreationOptions.None,
            TaskContinuationOptions.None,
            TaskScheduler.Default);

      public static void RunSync(Func<Task> func)
      {
         Factory
            .StartNew(func)
            .Unwrap()
            .GetAwaiter()
            .GetResult();
      }
